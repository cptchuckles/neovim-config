-- :help options
vim.opt.backup = false            -- creates a backup file
vim.opt.clipboard = "unnamedplus" -- uses the system clipboard
vim.opt.cmdheight = 2             -- more space in cmdline for messages
vim.opt.completeopt = { "menuone", "noselect" } -- cmp stuff
vim.opt.conceallevel = 0          -- makes `` visible in markdown
vim.opt.fileencoding = "utf-8"    -- file encoding
vim.opt.hlsearch = true           -- hilight last search query
vim.opt.ignorecase = true         -- case sensitivity in search
vim.opt.mouse = "a"               -- allows sacrilege
vim.opt.pumheight = 10            -- pop up menu height
vim.opt.showmode = true           -- shows current editing mode
vim.opt.showtabline = 2           -- always show tabs
vim.opt.smartcase = true          -- case-sensitive when search has caps
vim.opt.smartindent = true        -- hope it doesn't fuck me over
vim.opt.splitbelow = true         -- hsplit goes down, not up
vim.opt.splitright = false        -- vsplit goes right, not left
vim.opt.swapfile = false          -- create a swapfile while editing
